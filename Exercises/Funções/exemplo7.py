# Crie uma função que conte a quantidade de caracteres de uma frase
def conta_caracteres_frase(frase):
    quantidade_caracteres = len(frase)

    return quantidade_caracteres


resposta = conta_caracteres_frase("Buritizeiro Minas Gerais")
print(resposta)

resposta = conta_caracteres_frase("Pirapora Minas Gerais")
print(resposta)

resposta = conta_caracteres_frase("Programação com Python")
print(resposta)